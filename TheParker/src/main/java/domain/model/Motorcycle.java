package domain.model;

/**
 * Motorcycle descriptor class
 * @author juan.salazar
 *
 */
public class Motorcycle extends Vehicle {
	
	private Integer cc;

	public Motorcycle(String plate, String type, Integer cc) {
		super(plate, type);
		this.cc = cc;
	}
	
	public Integer getCc() {
		return cc;
	}

	public void setCc(Integer cc) {
		this.cc = cc;
	}

}
